# Generic vague regulator phrases to detect and flag
VAGUE_REGULATOR_PHRASES = [
    "the regulator",
    "the authority",
    "regulatory body",
    "the government",
    "the board",
    "authorities",
]


def validate_regulatory_specificity(text: str) -> list[str]:
    """Scan text for vague regulator references and return list of issues.

    Returns a list of actionable warnings to be surfaced in QA.
    Does not modify the text — that is the LLM re-generation's job.
    """
    text_lower = text.lower()
    issues = []
    for vague in VAGUE_REGULATOR_PHRASES:
        if vague in text_lower:
            # Find which domain context it appears in
            domain_hint = None
            for domain_kw, correct_body in REGULATOR_DOMAIN_MAP.items():
                if domain_kw in text_lower:
                    domain_hint = correct_body
                    break
            hint_str = f" (likely should be: {domain_hint})" if domain_hint else ""
            issues.append(
                f"[LEVEL 2] Vague regulator reference: '{vague}' found.{hint_str}"
            )
    return issues


def get_correct_regulator(domain: str) -> str:
    """Return the correct regulatory body name for a given domain.

    Used by the LLM prompt builder to inject the correct regulator into the
    system prompt context per spec 2.8.A Level 2.
    """
    domain_lower = domain.lower()
    for keyword, body in REGULATOR_DOMAIN_MAP.items():
        if keyword in domain_lower:
            return body
    return "Data Protection Board of India (DPBI)"  # safe default for DPDPA content


