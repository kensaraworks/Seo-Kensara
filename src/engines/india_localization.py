def get_compliance_calendar_context(milestone_keyword: Optional[str] = None) -> list[dict]:
    """Return the full or filtered DPDPA compliance calendar.

    If milestone_keyword is provided, returns only matching milestones.
    Each entry includes authoritative hedging language per spec 2.8.A Level 5.
    """
    if not milestone_keyword:
        return DPDPA_COMPLIANCE_CALENDAR
    keyword = milestone_keyword.lower()
    return [
        m for m in DPDPA_COMPLIANCE_CALENDAR
        if keyword in m["milestone"].lower() or keyword in m["description"].lower()
    ]


def get_hedged_deadline_phrase(milestone_keyword: str) -> Optional[str]:
    """Return the correctly-hedged deadline sentence for a milestone.

    Per spec 2.8.A Level 5 — all deadlines must use approved hedging language
    ('projected', 'expected', 'as per current DPDP Rules') to avoid legal flags
    while still conveying urgency.
    """
    results = get_compliance_calendar_context(milestone_keyword)
    if results:
        m = results[0]
        return (
            f"The {m['milestone']}, {m['hedge']} {m['projected_date']}, "
            f"means Indian businesses must act now to ensure readiness."
        )
    return None


