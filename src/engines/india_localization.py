def inject_business_scale_context(text: str) -> str:
    """Replace generic scale references with India-specific terminology.

    Per spec 2.8.A Level 3. Applied as a deterministic post-processing pass.
    """
    for pattern, replacement in BUSINESS_SCALE_REPLACEMENTS.items():
        text = re.sub(pattern, replacement, text, flags=re.IGNORECASE)
    return text


def get_business_scale_context_snippet(business_type: str) -> Optional[str]:
    """Return a context snippet for a specific business type.

    Consumed by the brief builder to inject into LLM system prompts.
    """
    key = business_type.lower().strip()
    for k, (label, snippet) in BUSINESS_SCALE_CONTEXT.items():
        if k in key:
            return snippet
    return None


