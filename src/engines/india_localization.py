# Master Injection Function
# ---------------------------------------------------------------------------

def build_india_context_block(
    sector: Optional[str] = None,
    business_type: Optional[str] = None,
    milestone_keyword: Optional[str] = None,
) -> dict:
    """Assemble the full India localization context block for LLM prompt injection.

    This is the primary output consumed by the brief builder (context/builder.py)
    and the blog writer to prepend authoritative India-specific context into
    every generation pass.

    Returns a structured dict with all 5 levels populated.
    """
    context = {
        "level_1_monetary_note": (
            "Express all financial figures as: ₹[X] crore (approximately $[Y] million USD). "
            "Never use 'INR'. Always use ₹ symbol. Never USD-only."
        ),
        "level_2_regulators": REGULATOR_DOMAIN_MAP,
        "level_3_business_scale": None,
        "level_4_sector_data": None,
        "level_5_calendar": DPDPA_COMPLIANCE_CALENDAR,
    }

    if business_type:
        context["level_3_business_scale"] = get_business_scale_context_snippet(business_type)

    if sector:
        sector_data = get_sector_context(sector)
        if sector_data:
            context["level_4_sector_data"] = sector_data
            log.info("india_context_sector_injected", sector=sector)

    if milestone_keyword:
        hedged = get_hedged_deadline_phrase(milestone_keyword)
        if hedged:
            context["level_5_deadline_phrase"] = hedged

    log.info("india_context_block_built", sector=sector, business_type=business_type)
    return context
