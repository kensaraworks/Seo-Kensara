def get_sector_context(sector: str) -> Optional[dict]:
    """Return the full sector context dict for a given industry.

    Consumed by the brief builder to inject pre-built statistics and the
    correct sector-specific regulator into LLM generation prompts.
    """
    key = sector.lower().strip()
    # Direct match
    if key in SECTOR_CONTEXT_LIBRARY:
        return SECTOR_CONTEXT_LIBRARY[key]
    # Fuzzy keyword match
    for sector_name, data in SECTOR_CONTEXT_LIBRARY.items():
        if sector_name in key or key in sector_name:
            return data
    return None


def get_sector_statistic(sector: str) -> Optional[str]:
    """Return just the statistic string for a sector."""
    ctx = get_sector_context(sector)
    return ctx["statistic"] if ctx else None


