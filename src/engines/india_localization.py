# Exchange rate constant — update when RBI rate changes significantly
# Deliberately held as a constant (not live-fetched) for determinism in tests.
USD_TO_INR = 84  # approximate 2025 rate

# Common DPDPA penalty figures in crore (for reference)
DPDPA_PENALTY_MAP = {
    "250 crore": (250, round(250 * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)),
    "200 crore": (200, round(200 * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)),
    "50 crore": (50, round(50 * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)),
    "10 crore": (10, round(10 * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)),
}

def format_monetary_figure(amount_inr_crore: float, include_usd: bool = True) -> str:
    """Format a monetary figure in ₹ first, then USD in parentheses.

    Per spec 2.8.A Level 1:
    - Never express in USD only
    - Never express in lakhs without also expressing in crore for large figures
    - Always use ₹ symbol, never 'INR'
    """
    crore_str = f"₹{amount_inr_crore:.0f} crore"
    if include_usd:
        usd_million = round(amount_inr_crore * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)
        return f"{crore_str} (approximately ${usd_million} million USD)"
    return crore_str


def enforce_monetary_framing(text: str) -> str:
    """Deterministic post-processing pass.

    Finds all bare USD amounts and adds ₹ conversion. Also finds ₹ amounts
    expressed without USD parenthetical and adds it. Critically, bare 'USD'
    or '$' amounts in legal penalty sentences are flagged/promoted to ₹-first.
    """
    # Pattern: bare USD amount like "$30 million" or "$30M"
    usd_pattern = re.compile(
        r'\$(\d+(?:\.\d+)?)\s*(?:million|M)\s*(?:USD)?', re.IGNORECASE
    )

    def _usd_to_inr_fmt(m: re.Match) -> str:
        usd_millions = float(m.group(1))
        crore = round((usd_millions * 1_000_000 * USD_TO_INR) / 1_00_00_000, 1)
        return f"₹{crore:.0f} crore (approximately ${usd_millions} million USD)"

    text = usd_pattern.sub(_usd_to_inr_fmt, text)

    # Pattern: ₹ amount without USD parenthetical in penalty sentences
    inr_pattern = re.compile(
        r'(₹(\d+(?:\.\d+)?)\s*crore)(?!\s*\(approximately)',
        re.IGNORECASE
    )

    def _add_usd_parenthetical(m: re.Match) -> str:
        crore_val = float(m.group(2))
        usd_m = round(crore_val * 1_00_00_000 / (USD_TO_INR * 1_000_000), 1)
        return f"{m.group(1)} (approximately ${usd_m} million USD)"

    text = inr_pattern.sub(_add_usd_parenthetical, text)
    return text


